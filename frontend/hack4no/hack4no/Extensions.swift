import Foundation
import UIKit

extension UIColor {
    
    // MARK: - Standard-Coloring-Colored
    class var hunter: UIColor {
        return UIColor(red: 81.0/255.0, green: 224.0/255.0, blue: 187.0/255.0, alpha: 1.0)
    }
    
    class var hunterLight: UIColor {
        return UIColor(red: 174.0/255.0, green: 255.0/255.0, blue: 134.0/255.0, alpha: 1.0)
    }
    
    class var hunterDark: UIColor {
        return UIColor(red: 57.0/255.0, green: 155.0/255.0, blue: 129.0/255.0, alpha: 1.0)
    }
    
    class var hunterGrey: UIColor {
        return UIColor(red: 159.0/255.0, green: 159.0/255.0, blue: 159.0/255.0, alpha: 1.0)
    }
}

